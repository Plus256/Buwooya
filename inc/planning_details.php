        Vaccinations & Malaria Medication
      </div>
      <div class="details_entry_caption_body">
        <p>
          Getting vaccinations is essential before going to Africa. The Center for Disease Control (CDC) also has detailed information on their website about immunizations recommended for traveling. Yellow fever is the only shot required to enter East Africa.
        </p>
        <p>
          The other following immunizations will usually be recommended:
          <ul>
            <li>Hepatitis B (series of 3 injections over several months, plan ahead)</li>
            <li>Hepatitis A (series of 2 injections)</li>
            <li>Adult polio booster (once in lifetime)</li>
            <li>MMR (Measles, Mumps and Rubella – make sure it is up to date)</li>
            <li>Typhoid (oral dosages are easier to tolerate. You need a repeat immunization every 5 years)</li>
          <li>Tetanus (renew immunization every 10 years)</li>
          </ul>
        </p>
        <p>
          Your physician, a local health clinic or a travel clinic is usually set up to handle travel immunizations and medications. We recommend that you ask them if they are equipped to do so when you call to make an appointment. Another thing you need to consider is that insurance doesn’t always cover all of the vaccinations. Travel clinics also generally don’t accept insurance. Sometimes you can submit claims afterwards, but that does not mean you are guaranteed a refund.
        </p>
        <p>
          o avoid malaria, you will need an adequate supply of anti-malaria tablets. Please consult your doctor as to which tablet to take, as they know your medical history best and some tablets have side effects.
        </p>
      </div>
      <div class="details_entry_caption_title">
        Money
      </div>
      <div class="details_entry_caption_body">
        <p>
          Depending on how long you are going to be in East Africa, we recommend that you bring between $100 - $200. It is better to bring spending money as cash in large bills: $50 and $100 bills dated after the year 2005 (Exchange bureaux do not accept bills dated before the year 2005.)
        </p>
        <p>
          Having extra cash will be very important for any tourist activities you’d like to participate in, e.g swimming, snacks bought in country, and meals outside the guesthouse. Having meals outside the guesthouse will be important if you begin to crave more westernized meals. You will also want to consider African crafts for yourself and as gifts for friends and family. Very nice crafts costs between $10 -$20 per item and small items can cost between $2 - $5. If you are coming for 3 months or more, it is best to withdraw your spending money out of an ATM. You will be charged foreign exchanges fees and other fees your bank may charge, so it is best to do this once a month.
        </p>
